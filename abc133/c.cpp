// constant
const int INF = (1<<30) - 1;
const int64 INF64 = (1LL<<62) - 1;

int main(){
    cout << fixed << setprecision(15);
    ios::sync_with_stdio(false);
    cin.tie(nullptr);

    int64 l, r;

    cin >> l >> r;

    int64 l2019 = (l / 2019) * 2019;
    l -= l2019;
    r -= l2019;

    if (r - l >= 2019) {
        cout << "0\n";
        return 0;
    }

    r -= ((r - l) / 2019) * 2019;

    int tmin = INF;

    for (int i = l; i < r; i++) {
        for (int j = i + 1; j <= r; j++) {
            int mod = (i * j) % 2019;
            if (mod < tmin) tmin = mod;
        }
    }

    cout << tmin << '\n';



    return 0;
}

